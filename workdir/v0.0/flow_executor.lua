local function inc_status(flow_id, status)
  local key = tostring(flow_id) .. ":" .. tostring(status)
  status_by_flow[key] = (status_by_flow[key] or 0) + 1
end

local function inc_failure(flow_id, reason)
  local key = tostring(flow_id) .. ":" .. tostring(reason)
  failures_by_flow[key] = (failures_by_flow[key] or 0) + 1
end

local function encode_status_map(m)
  local parts = {}
  for k, v in pairs(m) do
    parts[#parts + 1] = k .. "=" .. tostring(v)
  end
  -- Omit sort: cheaper on hot path; order is diagnostic-only.
  return table.concat(parts, ",")
end

local function encode_raw_step_map(m)
  local keys = {}
  for k, _ in pairs(m) do
    keys[#keys + 1] = tostring(k)
  end
  table.sort(keys)
  local lines = {}
  for _, k in ipairs(keys) do
    local vals = m[k]
    if vals and #vals > 0 then
      lines[#lines + 1] = k .. ":" .. table.concat(vals, ",")
    end
  end
  return table.concat(lines, ";")
end

local function encode_number_map(m)
  local keys = {}
  for k, _ in pairs(m) do
    keys[#keys + 1] = tostring(k)
  end
  table.sort(keys)
  local lines = {}
  for _, k in ipairs(keys) do
    lines[#lines + 1] = k .. "=" .. tostring(m[k] or 0)
  end
  return table.concat(lines, ",")
end

local function parse_keyed_csv_line(text, entry_sep, kv_sep)
  local out = {}
  if not text or text == "" then
    return out
  end
  for token in text:gmatch("([^" .. entry_sep .. "]+)") do
    local key, value = token:match("^(.-)" .. kv_sep .. "(.*)$")
    if key and value and key ~= "" then
      out[key] = value
    end
  end
  return out
end

local now_us
do
  local ok, ffi = pcall(require, "ffi")
  if ok then
    ffi.cdef([[
      typedef long time_t;
      typedef long suseconds_t;
      struct timeval {
        time_t tv_sec;
        suseconds_t tv_usec;
      };
      int gettimeofday(struct timeval *tv, void *tz);
    ]])
    local tv = ffi.new("struct timeval")
    now_us = function()
      ffi.C.gettimeofday(tv, nil)
      return (tonumber(tv.tv_sec) * 1000000) + tonumber(tv.tv_usec)
    end
  else
    now_us = function()
      return math.floor(os.clock() * 1000000)
    end
  end
end

local function json_error(msg)
  error("json parse error: " .. msg)
end

local function escape_json_string(s)
  local out = {}
  for i = 1, #s do
    local b = string.byte(s, i)
    if b == 34 then
      out[#out + 1] = '\\"'
    elseif b == 92 then
      out[#out + 1] = "\\\\"
    elseif b == 8 then
      out[#out + 1] = "\\b"
    elseif b == 12 then
      out[#out + 1] = "\\f"
    elseif b == 10 then
      out[#out + 1] = "\\n"
    elseif b == 13 then
      out[#out + 1] = "\\r"
    elseif b == 9 then
      out[#out + 1] = "\\t"
    elseif b < 32 then
      out[#out + 1] = string.format("\\u%04X", b)
    else
      out[#out + 1] = string.char(b)
    end
  end
  return table.concat(out)
end

-- Minimal JSON decoder (byte-oriented; avoids per-codeunit string.sub in tight loops).
local function decode_json(text)
  if text == nil then
    return nil
  end
  local i = 1
  local n = #text

  local function skip_ws()
    while i <= n do
      local b = string.byte(text, i)
      if b == 32 or b == 9 or b == 10 or b == 13 then
        i = i + 1
      else
        break
      end
    end
  end

  local function peek_byte()
    return string.byte(text, i)
  end

  local parse_value

  local function parse_string()
    if peek_byte() ~= 34 then
      json_error("expected string at " .. i)
    end
    i = i + 1
    local out = {}
    while i <= n do
      local b = peek_byte()
      if b == 34 then
        i = i + 1
        if #out == 0 then
          return ""
        end
        return table.concat(out)
      elseif b == 92 then
        i = i + 1
        if i > n then
          json_error("bad escape at end")
        end
        local esc = string.byte(text, i)
        if esc == 34 or esc == 92 or esc == 47 then
          out[#out + 1] = string.char(esc)
        elseif esc == 98 then
          out[#out + 1] = "\b"
        elseif esc == 102 then
          out[#out + 1] = "\f"
        elseif esc == 110 then
          out[#out + 1] = "\n"
        elseif esc == 114 then
          out[#out + 1] = "\r"
        elseif esc == 116 then
          out[#out + 1] = "\t"
        elseif esc == 117 then
          -- Keep \uXXXX escaped as literal; pointers for this workload are not unicode-sensitive.
          local hex = text:sub(i + 1, i + 4)
          if #hex ~= 4 then
            json_error("bad unicode escape at " .. i)
          end
          out[#out + 1] = "\\u" .. hex
          i = i + 4
        else
          json_error("unknown escape at " .. i)
        end
        i = i + 1
      else
        local j = i
        while i <= n do
          b = string.byte(text, i)
          if b == 34 or b == 92 then
            break
          end
          i = i + 1
        end
        if i > j then
          out[#out + 1] = text:sub(j, i - 1)
        end
      end
    end
    json_error("unterminated string")
  end

  local function parse_number()
    local s = i
    local b = peek_byte()
    if b == 45 then
      i = i + 1
    end
    while i <= n do
      b = string.byte(text, i)
      if b and b >= 48 and b <= 57 then
        i = i + 1
      else
        break
      end
    end
    if i <= n and peek_byte() == 46 then
      i = i + 1
      while i <= n do
        b = string.byte(text, i)
        if b and b >= 48 and b <= 57 then
          i = i + 1
        else
          break
        end
      end
    end
    if i <= n then
      b = peek_byte()
      if b == 101 or b == 69 then
        i = i + 1
        if i <= n then
          b = peek_byte()
          if b == 43 or b == 45 then
            i = i + 1
          end
        end
        while i <= n do
          b = string.byte(text, i)
          if b and b >= 48 and b <= 57 then
            i = i + 1
          else
            break
          end
        end
      end
    end
    local num = tonumber(text:sub(s, i - 1))
    if num == nil then
      json_error("bad number at " .. s)
    end
    return num
  end

  local function parse_array()
    if peek_byte() ~= 91 then
      json_error("expected '[' at " .. i)
    end
    i = i + 1
    skip_ws()
    local arr = {}
    if peek_byte() == 93 then
      i = i + 1
      return arr
    end
    while true do
      skip_ws()
      arr[#arr + 1] = parse_value()
      skip_ws()
      local b = peek_byte()
      if b == 44 then
        i = i + 1
      elseif b == 93 then
        i = i + 1
        return arr
      else
        json_error("expected ',' or ']' at " .. i)
      end
    end
  end

  local function parse_object()
    if peek_byte() ~= 123 then
      json_error("expected '{' at " .. i)
    end
    i = i + 1
    skip_ws()
    local obj = {}
    if peek_byte() == 125 then
      i = i + 1
      return obj
    end
    while true do
      skip_ws()
      local k = parse_string()
      skip_ws()
      if peek_byte() ~= 58 then
        json_error("expected ':' at " .. i)
      end
      i = i + 1
      skip_ws()
      obj[k] = parse_value()
      skip_ws()
      local b = peek_byte()
      if b == 44 then
        i = i + 1
      elseif b == 125 then
        i = i + 1
        return obj
      else
        json_error("expected ',' or '}' at " .. i)
      end
    end
  end

  function parse_value()
    skip_ws()
    local b = peek_byte()
    if b == 34 then
      return parse_string()
    elseif b == 123 then
      return parse_object()
    elseif b == 91 then
      return parse_array()
    elseif b == 45 or (b and b >= 48 and b <= 57) then
      return parse_number()
    elseif i + 3 <= n and text:sub(i, i + 3) == "true" then
      i = i + 4
      return true
    elseif i + 4 <= n and text:sub(i, i + 4) == "false" then
      i = i + 5
      return false
    elseif i + 3 <= n and text:sub(i, i + 3) == "null" then
      i = i + 4
      return nil
    end
    json_error("unexpected token at " .. i)
  end

  skip_ws()
  local value = parse_value()
  skip_ws()
  if i <= n then
    json_error("trailing content at " .. i)
  end
  return value
end


local function encode_json(value)
  local t = type(value)
  if t == "nil" then
    return "null"
  elseif t == "boolean" then
    return value and "true" or "false"
  elseif t == "number" then
    return tostring(value)
  elseif t == "string" then
    return '"' .. escape_json_string(value) .. '"'
  elseif t == "table" then
    -- Detect array-like table.
    local is_array = true
    local count = 0
    for k, _ in pairs(value) do
      if type(k) ~= "number" then
        is_array = false
        break
      end
      count = count + 1
    end
    if is_array then
      local parts = {}
      for i = 1, count do
        parts[#parts + 1] = encode_json(value[i])
      end
      return "[" .. table.concat(parts, ",") .. "]"
    end
    local keys = {}
    for k, _ in pairs(value) do
      keys[#keys + 1] = tostring(k)
    end
    table.sort(keys)
    local parts = {}
    for _, k in ipairs(keys) do
      parts[#parts + 1] = encode_json(k) .. ":" .. encode_json(value[k])
    end
    return "{" .. table.concat(parts, ",") .. "}"
  end
  return "null"
end

local function pointer_unescape(token)
  token = token:gsub("~1", "/")
  token = token:gsub("~0", "~")
  return token
end

local function resolve_json_pointer(root, pointer)
  if pointer == "" then
    return root
  end
  if type(pointer) ~= "string" or pointer:sub(1, 1) ~= "/" then
    return nil, "invalid pointer"
  end
  local current = root
  for token in pointer:gmatch("/([^/]*)") do
    token = pointer_unescape(token)
    if type(current) == "table" then
      if current[token] ~= nil then
        current = current[token]
      else
        local idx = tonumber(token)
        if idx and current[idx + 1] ~= nil then
          current = current[idx + 1]
        else
          return nil, "missing token '" .. token .. "'"
        end
      end
    else
      return nil, "cannot dereference non-table"
    end
  end
  return current, nil
end

local function clone(value)
  if type(value) ~= "table" then
    return value
  end
  local out = {}
  for k, v in pairs(value) do
    out[k] = clone(v)
  end
  return out
end

local function shallow_copy(t)
  if type(t) ~= "table" then
    return t
  end
  local out = {}
  for k, v in pairs(t) do
    out[k] = v
  end
  return out
end

local function parse_stage_step_reference(value)
  -- Format: <stage>.<flowId>.<source>#<jsonPointer>
  local stage, flow_id, source, pointer = value:match("^([^.]+)%.([^.]+)%.([^.]+)#(.*)$")
  if not stage then
    return nil
  end
  if source ~= "responseBody" and source ~= "endpoint" then
    return nil
  end
  if pointer == "" then
    pointer = "/"
  end
  if pointer:sub(1, 1) ~= "/" then
    return nil
  end
  return {
    stage = stage,
    flow_id = flow_id,
    source = source,
    pointer = pointer,
  }
end

local function resolve_reference_value(value, response_obj, st)
  if type(value) ~= "string" then
    return value, nil
  end
  if value:sub(1, 1) == "/" then
    if not response_obj then
      return nil, "pointer '" .. value .. "' requires previous response"
    end
    local resolved, err = resolve_json_pointer(response_obj, value)
    if err then
      return nil, err
    end
    return resolved, nil
  end

  local ref = parse_stage_step_reference(value)
  if not ref then
    return value, nil
  end

  if ref.stage ~= manifest.stage then
    return nil, "reference stage mismatch '" .. ref.stage .. "' != '" .. tostring(manifest.stage) .. "'"
  end
  if not st or type(st.step_outputs) ~= "table" then
    return nil, "reference state unavailable"
  end
  local step_out = st.step_outputs[ref.flow_id]
  if type(step_out) ~= "table" then
    return nil, "missing step output for flow '" .. ref.flow_id .. "'"
  end
  local source_obj = step_out[ref.source]
  if source_obj == nil then
    return nil, "missing source '" .. ref.source .. "' for flow '" .. ref.flow_id .. "'"
  end
  local resolved, err = resolve_json_pointer(source_obj, ref.pointer)
  if err then
    return nil, "reference pointer error: " .. err
  end
  return resolved, nil
end

local function substitute_pointers(value, response_obj, st)
  if type(value) == "string" then
    return resolve_reference_value(value, response_obj, st)
  end
  if type(value) ~= "table" then
    return value, nil
  end
  local out = nil
  for k, v in pairs(value) do
    local sv, err = substitute_pointers(v, response_obj, st)
    if err then
      return nil, err
    end
    if sv ~= v then
      if not out then
        out = {}
        for k2, v2 in pairs(value) do
          out[k2] = v2
        end
      end
      out[k] = sv
    end
  end
  return out or value, nil
end

local function url_encode(s)
  s = tostring(s)
  s = s:gsub("\n", "\r\n")
  s = s:gsub("([^%w%-_%.~])", function(c)
    return string.format("%%%02X", string.byte(c))
  end)
  return s
end

local function apply_path_params(path_template, params)
  local out = path_template
  for k, v in pairs(params or {}) do
    local token = "{" .. tostring(k) .. "}"
    out = out:gsub(token, url_encode(v))
  end
  return out
end

local function append_query(path, query)
  if not query then
    return path
  end
  local parts = {}
  for k, v in pairs(query) do
    if v ~= nil then
      parts[#parts + 1] = url_encode(k) .. "=" .. url_encode(v)
    end
  end
  if #parts == 0 then
    return path
  end
  table.sort(parts)
  return path .. "?" .. table.concat(parts, "&")
end

