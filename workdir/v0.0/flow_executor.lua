local function is_success_status(method, status)
  if success_mode == "http2xx3xx" then
    return status < 400
  end

  -- strict_expected mode, heuristic by HTTP verb.
  method = tostring(method or "")
  if method == "GET" then
    return status == 200 or status == 304
  elseif method == "POST" then
    return status == 200 or status == 201 or status == 202 or status == 204
  elseif method == "PUT" or method == "PATCH" then
    return status == 200 or status == 204
  elseif method == "DELETE" then
    return status == 200 or status == 202 or status == 204
  end
  return status < 400
end

local function body_snippet(text)
  if not text or #text == 0 then
    return ""
  end
  local s = tostring(text)
  s = s:gsub("\n", "\\n")
  s = s:gsub("\r", "\\r")
  if #s > max_body_snippet_bytes then
    return s:sub(1, max_body_snippet_bytes) .. "...(truncated)"
  end
  return s
end

local function log_failure(conn_id, st, reason, status, body)
  if not flow_failure_logs_enabled then
    return
  end
  if _G.fe_error_logs_emitted >= max_error_logs_per_thread then
    return
  end

  _G.fe_error_logs_emitted = _G.fe_error_logs_emitted + 1
  local iter_idx = st and st.iteration_index or -1
  local step_idx = st and st.current_step_index or -1
  local flow_id = st and st.current_flow_id or "unknown"
  local method = st and st.current_method or "?"
  local path = st and st.current_path or "?"
  print(string.format(
    "flow_failure thread=%d conn=%d iter=%s step=%s flow=%s method=%s path=%s status=%s reason=%s body=%s",
    thread_id, conn_id, tostring(iter_idx), tostring(step_idx), tostring(flow_id), tostring(method),
    tostring(path), tostring(status or "-"), tostring(reason), body_snippet(body)
  ))
end

local function ensure_conn_state(conn_id)
  local st = conn_state[conn_id]
  if st then
    return st
  end

  st = {
    assigned = false,
    exhausted = false,
    iteration_index = nil,
    step_index = 0,
    last_response = nil,
    last_error = nil,
    current_flow_id = nil,
    current_step_index = nil,
    current_method = nil,
    current_path = nil,
    current_endpoint_params = nil,
    current_step_started_us = nil,
    current_step_key = nil,
    step_outputs = {},
  }
  local cpthread = manifest.wrk2.connections_per_thread
  local start_index = ((thread_id - 1) * cpthread) + conn_id + 1
  st.next_iteration_index = start_index
  conn_state[conn_id] = st
  return st
end

local function claim_next_iteration(conn_id, st)
  if st.exhausted then
    return false
  end

  local raw_index = st.next_iteration_index
  local index = raw_index
  local total = #iterations
  if total == 0 then
    st.exhausted = true
    _G.fe_iterations_exhausted = _G.fe_iterations_exhausted + 1
    return false
  end

  if index > total then
    if reuse_mode == "wrap" then
      index = ((index - 1) % total) + 1
      _G.fe_iterations_reused = _G.fe_iterations_reused + 1
    else
      st.exhausted = true
      _G.fe_iterations_exhausted = _G.fe_iterations_exhausted + 1
      return false
    end
  end

  st.assigned = true
  st.iteration_index = index
  st.step_index = 1
  st.last_response = nil
  st.last_error = nil
  st.current_flow_id = nil
  st.current_step_index = nil
  st.current_method = nil
  st.current_path = nil
  st.current_endpoint_params = nil
  st.current_step_started_us = nil
  st.current_step_key = nil
  st.step_outputs = {}

  _G.fe_iterations_assigned = _G.fe_iterations_assigned + 1
  _G.fe_iterations_started = _G.fe_iterations_started + 1

  st.next_iteration_index = raw_index + manifest.wrk2.connections
  return true
end

local function fail_current_iteration(conn_id, st, reason, status, body)
  _G.fe_iterations_failed = _G.fe_iterations_failed + 1
  _G.fe_non_success_responses = _G.fe_non_success_responses + 1
  inc_failure(st.current_flow_id or "unknown", reason)
  st.last_error = reason
  log_failure(conn_id, st, reason, status, body)
  st.iteration_index = nil
  st.step_index = 0
  st.last_response = nil
  st.current_step_started_us = nil
  st.current_step_key = nil
end

local function complete_current_iteration(st)
  _G.fe_iterations_completed = _G.fe_iterations_completed + 1
  st.iteration_index = nil
  st.step_index = 0
  st.last_response = nil
  st.last_error = nil
  st.current_step_started_us = nil
  st.current_step_key = nil
end

local function record_step_elapsed(st)
  if not st then
    return
  end
  if not st.current_step_started_us or not st.current_step_key then
    return
  end
  local elapsed = now_us() - st.current_step_started_us
  if elapsed < 0 then
    elapsed = 0
  end
  local key = tostring(st.current_step_key)
  local values = step_latency_values_by_flow[key]
  if not values then
    values = {}
    step_latency_values_by_flow[key] = values
  end
  if #values < max_raw_step_samples_per_flow then
    values[#values + 1] = tostring(elapsed)
  else
    step_latency_dropped_by_flow[key] = (step_latency_dropped_by_flow[key] or 0) + 1
    _G.fe_step_raw_samples_dropped = (_G.fe_step_raw_samples_dropped or 0) + 1
  end
  st.current_step_started_us = nil
  st.current_step_key = nil
end

local function prepare_active_iteration(conn_id)
  local st = ensure_conn_state(conn_id)
  if st.iteration_index == nil then
    local ok = claim_next_iteration(conn_id, st)
    if not ok then
      return st, false
    end
  end
  return st, true
end

local function build_request_for_state(conn_id, st)
  local iter = iterations[st.iteration_index]
  local step = iter and iter.steps and iter.steps[st.step_index]
  if not step then
    fail_current_iteration(conn_id, st, "missing_step", nil, nil)
    return nil
  end

  local path_params, err = substitute_pointers(step.pathParameters or EMPTY_TABLE, st.last_response, st)
  if err then
    _G.fe_pointer_resolve_failures = _G.fe_pointer_resolve_failures + 1
    fail_current_iteration(conn_id, st, "path_pointer:" .. err, nil, nil)
    return nil
  end

  local query, qerr = substitute_pointers(step.query or EMPTY_TABLE, st.last_response, st)
  if qerr then
    _G.fe_pointer_resolve_failures = _G.fe_pointer_resolve_failures + 1
    fail_current_iteration(conn_id, st, "query_pointer:" .. qerr, nil, nil)
    return nil
  end

  local headers, herr = substitute_pointers(step.headers or EMPTY_TABLE, st.last_response, st)
  if herr then
    _G.fe_pointer_resolve_failures = _G.fe_pointer_resolve_failures + 1
    fail_current_iteration(conn_id, st, "header_pointer:" .. herr, nil, nil)
    return nil
  end

  local req_body, berr = substitute_pointers(step.requestBody, st.last_response, st)
  if berr then
    _G.fe_pointer_resolve_failures = _G.fe_pointer_resolve_failures + 1
    fail_current_iteration(conn_id, st, "body_pointer:" .. berr, nil, nil)
    return nil
  end

  local final_headers = shallow_copy(base_headers)
  for k, v in pairs(headers or {}) do
    final_headers[k] = tostring(v)
  end

  local body_text = nil
  if req_body ~= nil then
    local ok, encoded = pcall(encode_json, req_body)
    if not ok then
      _G.fe_template_fill_failures = _G.fe_template_fill_failures + 1
      fail_current_iteration(conn_id, st, "body_encode_failed", nil, nil)
      return nil
    end
    body_text = encoded
    if final_headers["Content-Type"] == nil then
      final_headers["Content-Type"] = "application/json"
    end
  end

  local path = apply_path_params(step.pathTemplate, path_params)
  path = append_query(path, query)
  path = "/petclinic/api" .. path

  st.current_flow_id = step.flowId
  st.current_step_index = st.step_index
  st.current_method = step.method
  st.current_path = path
  st.current_endpoint_params = shallow_copy(path_params or EMPTY_TABLE)
  st.current_step_started_us = now_us()
  st.current_step_key = tostring(step.flowId or "unknown")

  return wrk.format(step.method, path, final_headers, body_text)
end

local function finalize_thread_status()
  _G.fe_status_line = encode_status_map(status_by_flow)
  _G.fe_failure_line = encode_status_map(failures_by_flow)
  _G.fe_step_raw_line = encode_raw_step_map(step_latency_values_by_flow)
  _G.fe_step_raw_dropped_line = encode_number_map(step_latency_dropped_by_flow)
end

local function active_iterations()
  local started = _G.fe_iterations_started or 0
  local completed = _G.fe_iterations_completed or 0
  local failed = _G.fe_iterations_failed or 0
  local active = started - completed - failed
  if active < 0 then
    return 0
  end
  return active
end

local function update_status_snapshot()
  _G.fe_active_iterations = active_iterations()
  finalize_thread_status()
end

local function maybe_prepare_or_idle(conn_id)
  local st, ok = prepare_active_iteration(conn_id)
  if not ok then
    return st, nil
  end
  local req = build_request_for_state(conn_id, st)
  return st, req
end

local function request_with_retries(conn_id, retries)
  for _ = 1, retries do
    local st, req = maybe_prepare_or_idle(conn_id)
    if req ~= nil then
      return req
    end
    if st and st.exhausted then
      return idle_request
    end
  end
  return idle_request
end

local function on_success_response(st, parsed)
  if st.current_flow_id then
    st.step_outputs[st.current_flow_id] = {
      responseBody = parsed,
      endpoint = shallow_copy(st.current_endpoint_params or EMPTY_TABLE),
    }
  end
  st.last_response = parsed
  st.step_index = st.step_index + 1
  local iter = iterations[st.iteration_index]
  if iter and st.step_index > #iter.steps then
    complete_current_iteration(st)
  end
end

local function on_non_success_response(conn_id, st, status, body)
  fail_current_iteration(conn_id, st, "http_status", status, body)
end

local function after_response_bookkeeping()
  update_status_snapshot()
end

local function on_decode_response_body(body)
  if not body or #body == 0 then
    return nil
  end
  local ok, decoded = pcall(decode_json, body)
  if ok then
    return decoded
  end
  return nil
end

local function response_flow(conn_id, st, status, body)
  record_step_elapsed(st)
  if st.current_flow_id then
    inc_status(st.current_flow_id, status)
  end
  if is_success_status(st.current_method, status) then
    local parsed = on_decode_response_body(body)
    on_success_response(st, parsed)
  else
    on_non_success_response(conn_id, st, status, body)
  end
end

local function init_runtime_counters()
  _G.fe_iterations_assigned = 0
  _G.fe_iterations_started = 0
  _G.fe_iterations_completed = 0
  _G.fe_iterations_failed = 0
  _G.fe_iterations_reused = 0
  _G.fe_iterations_exhausted = 0
  _G.fe_pointer_resolve_failures = 0
  _G.fe_template_fill_failures = 0
  _G.fe_non_success_responses = 0
  _G.fe_error_logs_emitted = 0
  _G.fe_status_line = ""
  _G.fe_failure_line = ""
  _G.fe_step_raw_line = ""
  _G.fe_step_raw_dropped_line = ""
  _G.fe_step_raw_samples_dropped = 0
  _G.fe_active_iterations = 0
  _G.fe_max_conn_seen = -1
end

function setup(thread)
  setup_thread_counter = setup_thread_counter + 1
  thread:set("flow_thread_id", setup_thread_counter)
  threads[#threads + 1] = thread
end

function init(args)
  local host = wrk.host
  local port = wrk.port
  host = host:find(":") and ("[" .. host .. "]") or host
  host = port and (host .. ":" .. port) or host
  wrk.headers["Host"] = host
  base_headers["Host"] = host
  base_headers["Accept"] = "application/json"

  thread_id = tonumber(flow_thread_id) or 0
  if thread_id <= 0 then
    error("flow_thread_id missing; setup(thread) was not executed")
  end

  for _, a in ipairs(args or {}) do
    if a == "--flow-failure-logs" then
      flow_failure_logs_enabled = true
    elseif a == "--no-flow-failure-logs" then
      flow_failure_logs_enabled = false
    end
  end

  init_runtime_counters()

  idle_request = make_idle_request()
end

function request(conn_id)
  if conn_id > _G.fe_max_conn_seen then
    _G.fe_max_conn_seen = conn_id
  end
  return request_with_retries(conn_id, 3)
end

function response(status, headers, body, conn_id)
  local st = conn_state[conn_id]
  if not st or st.iteration_index == nil then
    return
  end

  response_flow(conn_id, st, status, body)
  after_response_bookkeeping()
end

function done(summary, latency, requests)
  local assigned = 0
  local started = 0
  local completed = 0
  local failed = 0
  local reused = 0
  local exhausted = 0
  local ptr_fail = 0
  local tpl_fail = 0
  local non_success = 0
  local logs_emitted = 0
  local active = 0
  local expected_connections_seen = 0
  local status_lines = {}
  local failure_lines = {}
  local raw_by_flow = {}
  local dropped_by_flow = {}
  local dropped_total = 0

  for idx, thread in ipairs(threads) do
    assigned = assigned + (thread:get("fe_iterations_assigned") or 0)
    started = started + (thread:get("fe_iterations_started") or 0)
    completed = completed + (thread:get("fe_iterations_completed") or 0)
    failed = failed + (thread:get("fe_iterations_failed") or 0)
    reused = reused + (thread:get("fe_iterations_reused") or 0)
    exhausted = exhausted + (thread:get("fe_iterations_exhausted") or 0)
    ptr_fail = ptr_fail + (thread:get("fe_pointer_resolve_failures") or 0)
    tpl_fail = tpl_fail + (thread:get("fe_template_fill_failures") or 0)
    non_success = non_success + (thread:get("fe_non_success_responses") or 0)
    logs_emitted = logs_emitted + (thread:get("fe_error_logs_emitted") or 0)
    active = active + (thread:get("fe_active_iterations") or 0)
    local max_conn = thread:get("fe_max_conn_seen") or -1
    if max_conn >= 0 then
      expected_connections_seen = expected_connections_seen + (max_conn + 1)
    end
    local line = thread:get("fe_status_line")
    if line and line ~= "" then
      status_lines[#status_lines + 1] = string.format("  thread %d status_by_flow: %s", idx, line)
    end
    local fline = thread:get("fe_failure_line")
    if fline and fline ~= "" then
      failure_lines[#failure_lines + 1] = string.format("  thread %d failures_by_flow: %s", idx, fline)
    end
    local raw_line = thread:get("fe_step_raw_line")
    local raw_pairs = parse_keyed_csv_line(raw_line, ";", ":")
    for flow_id, csv in pairs(raw_pairs) do
      if csv ~= "" then
        local prev = raw_by_flow[flow_id]
        if prev and prev ~= "" then
          raw_by_flow[flow_id] = prev .. "," .. csv
        else
          raw_by_flow[flow_id] = csv
        end
      end
    end
    local dropped_line = thread:get("fe_step_raw_dropped_line")
    local dropped_pairs = parse_keyed_csv_line(dropped_line, ",", "=")
    for flow_id, count_text in pairs(dropped_pairs) do
      local inc = tonumber(count_text) or 0
      dropped_by_flow[flow_id] = (dropped_by_flow[flow_id] or 0) + inc
    end
    dropped_total = dropped_total + (thread:get("fe_step_raw_samples_dropped") or 0)
  end

  print("")
  print("--- flow_executor ---")
  print(string.format("iterations_assigned: %d", assigned))
  print(string.format("iterations_started: %d", started))
  print(string.format("iterations_completed: %d", completed))
  print(string.format("iterations_failed: %d", failed))
  print(string.format("iterations_reused: %d", reused))
  print(string.format("iterations_exhausted: %d", exhausted))
  print(string.format("pointer_resolve_failures: %d", ptr_fail))
  print(string.format("template_fill_failures: %d", tpl_fail))
  print(string.format("non_success_responses: %d", non_success))
  print(string.format("error_logs_emitted: %d", logs_emitted))
  print(string.format("active_iterations_at_done: %d", active))
  for _, line in ipairs(status_lines) do
    print(line)
  end
  for _, line in ipairs(failure_lines) do
    print(line)
  end
  local raw_keys = {}
  for k, _ in pairs(raw_by_flow) do
    raw_keys[#raw_keys + 1] = k
  end
  table.sort(raw_keys)
  for _, flow_id in ipairs(raw_keys) do
    print(string.format("step_raw_us %s: %s", flow_id, raw_by_flow[flow_id]))
  end
  local dropped_keys = {}
  for k, _ in pairs(dropped_by_flow) do
    dropped_keys[#dropped_keys + 1] = k
  end
  table.sort(dropped_keys)
  for _, flow_id in ipairs(dropped_keys) do
    print(string.format("step_raw_samples_dropped %s: %d", flow_id, dropped_by_flow[flow_id] or 0))
  end
  print(string.format("step_raw_samples_dropped_total: %d", dropped_total))
  print(string.format("connections_seen: %d", expected_connections_seen))
  print(string.format("partition_ok: %s", (expected_connections_seen > 0 and assigned >= expected_connections_seen) and "true" or "false"))
  print(string.format("accounting_ok: %s", (started >= (completed + failed)) and "true" or "false"))
end

